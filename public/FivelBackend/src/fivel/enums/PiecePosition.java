package fivel.enums;

/**
 * Enumerator for slots' position on a tile.
 * 
 * @author Shay Davidson, Moshe Lichman and Tom Teman.
 * Game Intelligence course, TAU 2010/11a.
 */
public enum PiecePosition
{
	UpperLeft(0),   // Upper left corner.
	UpperRight(1),  // Upper right corner.
	BottomRight(2), // Bottom right corner.
	BottomLeft(3);  // Bottom left corner.
	
	private int m_id; // ID.
		
	/**
	 * Constructor.
	 * 
	 * @param id Enumerator id.
	 */
	PiecePosition(int id)
	{		
		m_id = id;
	}
	
	/**
	 * @return the enumerator id.
	 */
	public int getID()
	{
		return m_id;
	}	
}
